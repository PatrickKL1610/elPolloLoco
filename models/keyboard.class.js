/**
 * Represents the keyboard state for controlling game actions.
 */
class Keyboard {
    /**
     * Indicates whether the left arrow key is pressed.
     * @type {boolean}
     */
    LEFT = false;

    /**
     * Indicates whether the right arrow key is pressed.
     * @type {boolean}
     */
    RIGHT = false;

    /**
     * Indicates whether the up arrow key is pressed.
     * @type {boolean}
     */
    UP = false;

    /**
     * Indicates whether the spacebar key is pressed (used for jumping).
     * @type {boolean}
     */
    SPACE = false;

    /**
     * Indicates whether the enter key is pressed.
     * @type {boolean}
     */
    ENTER = false;

    /**
     * Indicates whether the 'A' key is pressed (used for moving left).
     * @type {boolean}
     */
    A = false;

    /**
     * Indicates whether the 'D' key is pressed (used for moving right).
     * @type {boolean}
     */
    D = false;

    /**
     * Indicates whether the mute key is pressed.
     * @type {boolean}
     */
    MUTE = false;
}
